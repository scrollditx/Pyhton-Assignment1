# WAP to Find data type for Particular Variable[integer,Boolean,float,string, complex]

integer_var = 10
boolean_var = True
float_var = 4.11
string_var = "Hello, Python"
complex_var = 3 + 5*(boolean_var) # Complex Remaining

print(f"The data type of integer_var is: {type (integer_var)}")
print(f"The data type of boolean_var is: {type (boolean_var)}")
print(f"The data type of float_var is: {type (float_var)}")
print(f"The data type of string_var is: {type (string_var)}")
print(f"The data type of complex_var is: {type (complex_var)}") # Complex Remaining